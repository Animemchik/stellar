use ry_interner::Symbol;
use ty::Type;

pub mod ty;

#[derive(Debug, PartialEq, Eq, Clone)]
pub struct Path {
    symbols: Vec<Symbol>,
}

#[derive(Debug, PartialEq, Clone)]
pub struct TypePath {
    segments: Vec<TypePathSegment>,
}

#[derive(Debug, PartialEq, Clone)]
pub struct TypePathSegment {
    left: Path,
    right: Vec<Type>,
}

pub type TypeBounds = Vec<TypePath>;
